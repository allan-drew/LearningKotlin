package eCollections

// LIST:
    // é indexada
    // dinâmica. É como se fosse um array com api mais rica
    // Pode ser homogênea (regra) --> dados do mesmo tipo
    // Pode ser heterogênea --> dados com tipos diferentes
    // aceita objetos duplicados
    // por padrão é ordenado pela ordem de inserção. Ao adicionarmos, ele irá para o final da lista

// SET:
    // não indexada
    // pode ser ordenado (mas a implementação mais comum é não ordenada)
    // Pode ser homogênea (regra) --> dados do mesmo tipo
    // Pode ser heterogênea --> dados com tipos diferentes
    // não aceita objetos duplicados

// MAP:
    // estrutura de chave e valor
    // dinâmica
    // Pode ser homogênea (regra) --> dados do mesmo tipo
    // Pode ser heterogênea --> dados com tipos diferentes
